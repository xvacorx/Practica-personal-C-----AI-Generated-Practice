//Ejercicio 1:
//Escribe un programa en C# que solicite al usuario un número entero positivo
//y calcule la suma de todos los números enteros desde 1 hasta ese número ingresado.

int Suma = 0;

Console.WriteLine("Ingrese un número:");
int N = int.Parse(Console.ReadLine());

for  (int i = 0; i <= N; i++)
{
    Suma += i;
}

Console.WriteLine("La suma de todos los enteros es de: " + Suma);
